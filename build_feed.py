ORDER = ["Category", "ServiceType", "ServiceSubtype", "TypesOfWebsites",
         "TypesOfDevelopment", "ContentManagementSystem", "WorkExperience",
         "Place", "WorkWith", "Consultations", "WorkWithContract", "Prepayment"]


def build():
    out = ['<?xml version="1.0" encoding="UTF-8"?>',
           '<Ads formatVersion="3" target="Avito.ru">']
    for i, ad in enumerate(ads, 1):
        out.append("  <Ad>")
        out.append(f"    <Id>{ad['id']}</Id>")
        out.append(f"    <Title>{escape(ad['title'])}</Title>")
        out.append(f"    <Description><![CDATA[{ad['desc']}]]></Description>")
        out.append(f"    <Category>{escape(COMMON['Category'])}</Category>")
        if ad["price"] is not None:
            out.append(f"    <Price>{ad['price']}</Price>")
        out.append(f"    <Address>{escape(ADDRESS)}</Address>")
        out.append(f"    <ContactPhone>{CONTACT_PHONE}</ContactPhone>")
        out.append(f"    <ContactMethod>{escape(CONTACT_METHOD)}</ContactMethod>")
        # параметры подкатегории (Category уже выведен выше)
        for tag in ORDER[1:]:
            out.append(f"    <{tag}>{escape(COMMON[tag])}</{tag}>")
        out.append("    <Images>")
        out.append(f'      <Image url="{RAW}/ad-{i}.png"/>')
        out.append("    </Images>")
        out.append("  </Ad>")
    out.append("</Ads>")
    return "\n".join(out) + "\n"


if __name__ == "__main__":
    xml = build()
    with open("feed.xml", "w", encoding="utf-8") as f:
        f.write(xml)
    print("feed.xml written, ads:", len(ads))
